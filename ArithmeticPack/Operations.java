package ArithmeticPack;

public class Operations
{
	public int add(int a,int b)
	{
		return(a+b);
	}
		
	
	public int difference(int a,int b)
	{
		return(a-b);
	}
	public int division(int a,int b)
	{
		return(a/b);
	}
	public int multiplicate(int a,int b)
	{
		return(a*b);
	}

}